function sumArrays(arr1, arr2) {
		var sum = [];
		// if not same length, return
		if (arr1.length != arr2.length)
			throw "arrays are not of the same length";
		for (var i = 0; i < arr1.length; i++) {
			sum[i] = arr1[i] + arr2[i];
		}
		return sum;
}

